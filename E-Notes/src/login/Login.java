    private void btnRegistrarseCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseCActionPerformed
        if (jtUsuarioC.getText().equals("Nombre de Usuario") || jtNombreC.getText().equals("Nombre y Apellido") || jtEmailC.getText().equals("E-mail") || jtPassC.getText().equals("Contraseña")) {
            JOptionPane.showMessageDialog(null, "Rellene todo los campos");
        } else {
            if (!existeUsuario(jtUsuarioC.getText())) {
                try {
                    String consulta = "INSERT INTO USUARIO VALUES ('" + jtUsuarioC.getText() + "','" + jtNombreC.getText() + "','" + jtEmailC.getText() + "','" + jtPassC.getText() + "')";

                    PreparedStatement sql = Conexion.getConnection().prepareStatement(consulta);

                    sql.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Usuario creado correctamente!");
                    cards.removeAll();
                    cards.add(pnlIngresar);
                    cards.repaint();
                    cards.revalidate();
                    cardSelected();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.toString());
                }
            } else {
                JOptionPane.showMessageDialog(null, "El nombre de usuario que eligió ya existe, por favor elija otro");
            }
        }
    }//GEN-LAST:event_btnRegistrarseCActionPerformed

