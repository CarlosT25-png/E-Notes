    private void btnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccederActionPerformed
        int resul;
        if(jtUsuario.getText().equals("E-Mail") || jtUsuario.getText().equals("") || jpsPass.getText().equals("") || jpsPass.getText().equals("$$$$$$$$$$$$$")){
            JOptionPane.showMessageDialog(null, "Rellene todo los campos");
        }else{
            try {
                Statement sql = Conexion.getConnection().createStatement();

                String consulta = "SELECT U.NOMBRE_USUARIO FROM USUARIO AS U WHERE U.EMAIL='" + jtUsuario.getText() + "' AND PASSWORD='" + jpsPass.getText() + "'";
                ResultSet resultado = sql.executeQuery(consulta);

                if (resultado.next()) {
                    resul = 1;
                    nUsuario=resultado.getString(1);
                }else{
                    resul=0;
                }
                
                if(resul==1){
                    MenuPrincipal ventana = new MenuPrincipal();
                    ventana.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Datos de la cuenta invalidos");
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Datos de la cuenta invalidos");
            }
        }
    }//GEN-LAST:event_btnAccederActionPerformed
    private boolean existeUsuario(String user){
        boolean existe=false;
        try {
            Statement sql = Conexion.getConnection().createStatement();

            String consulta = "SELECT * FROM USUARIO AS U WHERE U.NOMBRE_USUARIO='" + user + "'";
            ResultSet resultado = sql.executeQuery(consulta);

            if (resultado.next()) {
                existe = true;
            } else {
                existe = false;
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Datos de la cuenta invalidos");
        }
        
        return existe;
    }

